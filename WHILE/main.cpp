//program perulangan WHILE
#include <iostream> //library input output program c++

using namespace std; //meringkas std pada program c++

int main()//program utama c++
{//membuka fungsi
   int i=0; //membuat inisialisasi variabel i dari 0

    while(i<7){ //kondisi perulangan sebanyak 7x
        cout<<"aku suka tidur"<<endl; //menyatakan perulangan kalimat aku suka tidur
        i++; //increment var i
    }//tutup fungsi
    return 0;//menandakan program berjalan dengan baik
}//tutup fungsi
